async function seed() {
    try {
        const uri = process.env.MONGODB_URI;
        if (!uri) throw new Error("MONGODB_URI is not defined");
        await mongoose.connect(uri);
        console.log('Connected to MongoDB.');

        const rawDataPath = path.join(__dirname, 'islamic_raw.txt');
        const fileContent = fs.readFileSync(rawDataPath, 'utf8');

        // Simple extraction of objects from the raw concatenated format
        // We'll use a regex to find all JSON-like objects {}
        const objects = [];
        const matches = fileContent.match(/\{[\s\S]*?\}/g);
        if (matches) {
            matches.forEach((m, idx) => {
                try {
                    objects.push(JSON.parse(m));
                } catch (e) {
                    console.error(`Failed to parse object at index ${idx}:`, m.substring(0, 50) + "...");
                }
            });
        }

        console.log(`Extracted ${objects.length} raw objects from file.`);

        const uniqueQuestions = new Map();
        const formattedQuestions = [];

        for (const obj of objects) {
            if (!obj.question || !obj.options || !obj.answer) {
                console.log(`Skipping incomplete object: ${JSON.stringify(obj).substring(0, 50)}...`);
                continue;
            }

            const qText = obj.question.trim().replace(/\s+/g, ' '); // Normalize whitespace
            if (uniqueQuestions.has(qText)) {
                console.log(`Skipping duplicate text: "${qText.substring(0, 30)}..."`);
                continue;
            }

            const correctIdx = obj.options.indexOf(obj.answer);
            if (correctIdx === -1) {
                console.error(`Correct answer not found in options for: ${qText}`);
                continue;
            }

            uniqueQuestions.set(qText, true);
            formattedQuestions.push({
                question: qText,
                options: obj.options,
                correctIndex: correctIdx,
                category: 'Islamic',
                level: Math.floor(Math.random() * 5) + 1,
                medium: 'Malayalam'
            });
        }

        console.log(`Prepared ${formattedQuestions.length} unique questions for seeding.`);

        if (formattedQuestions.length > 0) {
            // Clear existing Islamic questions to ensure a clean slate of exactly 300 unique questions
            await Question.deleteMany({ category: 'Islamic' });
            
            const result = await Question.insertMany(formattedQuestions);
            console.log(`Successfully seeded ${result.length} questions.`);
        }

    } catch (error) {
        console.error('Seeding error:', error);
    } finally {
        mongoose.disconnect();
    }
}

seed();
