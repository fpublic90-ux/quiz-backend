async function seedSocialScience2Malayalam() {
    try {
        await mongoose.connect(MONGODB_URI);
        console.log('Connected to MongoDB');

        const parsedQuestions = [];
        for (let item of rawData) {
            const correctIndex = item.options.indexOf(item.answer);
            parsedQuestions.push({
                question: item.question,
                options: item.options,
                correctIndex: correctIndex !== -1 ? correctIndex : 0,
                level: 1,
                board: "Kerala State",
                class: "10th (SSLC)",
                subject: "Social Science II",
                chapter: item.chapter,
                medium: "Malayalam"
            });
        }

        let addedCount = 0;
        let skippedCount = 0;

        for (const q of parsedQuestions) {
            const exists = await Question.findOne({
                question: q.question,
                subject: q.subject,
                medium: q.medium,
                board: q.board,
                class: q.class
            });

            if (!exists) {
                await Question.create(q);
                addedCount++;
                if (addedCount % 10 === 0) {
                    console.log(`Progress: Added ${addedCount} questions...`);
                }
            } else {
                skippedCount++;
            }
        }

        console.log('--------------------------------------------------');
        console.log('Social Science II Malayalam seeding completed!');
        console.log(`Total Added: ${addedCount}`);
        console.log(`Total Skipped: ${skippedCount}`);
        console.log('--------------------------------------------------');
    } catch (error) {
        console.error('Error seeding data:', error);
    } finally {
        await mongoose.disconnect();
    }
}

seedSocialScience2Malayalam();
